function addNum(num1=0,num2=0){
    return num1 + num2;
}

function mulNum(num1,num2){
    return num1*num2;
}

function divNum(num1,num2){
    return parseInt(num1/num2);
}

function subNum(num1,num2){
    return num1-num2;
}



console.log(addNum(5,6));
console.log(mulNum(5,6));
console.log(subNum(14,6));
console.log(divNum(15,3));
